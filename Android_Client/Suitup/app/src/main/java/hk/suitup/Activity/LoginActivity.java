    /**
     * Represents an asynchronous login/registration task used to authenticate
     * the user.
     */
    public class UserLoginTask extends AsyncTask<Void, Void, Boolean> {

        private final String mEmail;
        private final String mPassword;
        private String response_str;

        UserLoginTask(String email, String password) {
            mEmail = email;
            mPassword = password;
        }

        private  Boolean dealResponse(String response){
            JsonReader jsonReader = new JsonReader(new StringReader(response));
            try {
                jsonReader.beginArray();
                String valid;
                //check the response is success or not
                while (jsonReader.hasNext()){
                    jsonReader.beginObject();
                    while(jsonReader.hasNext()){
                        String tagName = jsonReader.nextName();
                        if(tagName.equals("ret")){
                            valid=jsonReader.nextString();
                            if(valid.equals("success")){
                                System.out.print(valid);
                                return true;
                            }
                        }
                    }
                    jsonReader.endObject();
                }
                jsonReader.endArray();

            }
            catch (Exception e){
                return false;
            }
            return false;
        }

        @Override
        protected Boolean doInBackground(Void... params) {


            try {
                //network access with SSL using bks

                LoginService loginService = new LoginService(mEmail,mPassword);

                SSLClient sslClient = new SSLClient(getApplicationContext());
                SSLClient.sendMessage(loginService.formLength()+"\r\n");
                SSLClient.sendMessage(loginService.getTemp());

                //get message length
                int response_length = sslClient.getMessageLength();
                BufferedReader reader = sslClient.getReader();

                //get response
                char[] response = new char[response_length];
                reader.read(response, 0, response_length);

                response_str = new String(response);
                return dealResponse(response_str);

            } catch (Exception e) {
                return false;
            }

        }

        @Override
        protected void onPostExecute(final Boolean success) {
            mAuthTask = null;
            showProgress(false);

            if (success) {
                try {
                    //jump to the main activity
                    Intent intent = new Intent(getApplicationContext(),MainActivity.class);
                    //String test = response_str;
                    Bundle mBundle = new Bundle();

                    //Here to construct the user information
                    JsonParser jsonParser = new JsonParser();
                    JsonArray array = jsonParser.parse(response_str).getAsJsonArray();
                    JsonObject userinfo = jsonParser.parse(array.get(1).toString()).getAsJsonObject();

                    Gson gson = new Gson();
                    User user = gson.fromJson(userinfo, User.class);

                    mBundle.putSerializable("user", user);
                    intent.putExtras(mBundle);

                    startActivity(intent);

                    System.out.print("+ = ");
                }
                catch(Exception e){
                    return ;
                }
                //finish();
            } else {
                mPasswordView.setError(getString(R.string.error_incorrect_password));
                mPasswordView.requestFocus();
            }
        }

        @Override
        protected void onCancelled() {
            mAuthTask = null;
            showProgress(false);
        }

    }

